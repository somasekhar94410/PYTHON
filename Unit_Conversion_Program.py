kilometers = float(input("Enter distance in kilometers: "))
meters = float(input("Enter distance in meters: "))
kilograms = float(input("Enter weight in kilograms: "))

km_to_m = kilometers * 1000
m_to_cm = meters * 100
kg_to_g = kilograms * 1000

print("Kilometers to Meters =", km_to_m)
print("Meters to Centimeters =", m_to_cm)
print("Kilograms to Grams =", kg_to_g)
